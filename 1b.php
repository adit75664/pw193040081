<?php

$npm = 81;
$kali = 5;
$bagi = 2;
$tambah = 75;
$dikurang = 20;

Echo "Aku adalah angka <b>$npm</b><br>";

$hasilKali = $npm * $kali;
Echo "Jika aku dikali $kali, maka aku sekarang menjadi <b>$hasilKali</b><br>";

$hasilBagi = $hasilKali / $bagi;
Echo "Jika aku dibagi $bagi, maka aku sekarang menjadi <b>$hasilBagi</b><br>";

$hasilTambah = $hasilBagi + $tambah;
Echo "Jika aku dikali $tambah, maka aku sekarang menjadi <b>$hasilTambah</b><br>";

$hasilDikurang = $hasilTambah - $dikurang;
Echo "Jika aku dikali $dikurang, maka aku sekarang menjadi <b>$hasilDikurang</b><br>";

?>